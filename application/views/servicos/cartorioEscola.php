            <h5><strong>Cartório na Escola</strong></h5>
            <p>"Cartório na Escola" é um projeto do Tabelião Del Guércio, visando expor de maneira simples e singela, por histórias em quadrinhos, alguns dos serviços prestados no cartório de notas e protesto. O Tabelião Arthur Del Guércio Neto é um grande entusiasta da ideia de difundir o que pode ser feito no cartório, campo desconhecido pela maioria da população. Para tanto, escreve artigos de fácil compreensão para jornais e revistas, promove palestras nos mais variados segmentos da sociedade, além de inúmeras outras ações. O foco dessa vez são as crianças, as quais, em algum momento de sua vida, precisarão do cartório. Serão vários gibizinhos, os quais contém uma pequena história, além de pensamentos relevantes e até mesmo alguns jogos. A distribuição será feita aos clientes do cartório, em escolas do Município de Itaquaquecetuba, bem como nos canais de comunicação da internet do cartório.</p>
