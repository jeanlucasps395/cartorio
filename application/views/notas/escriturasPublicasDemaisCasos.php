			<div class="content__title">
				<strong>Conceito</strong>
			</div>
			<p>As escrituras públicas são atos notariais que formalizam juridicamente atos e negócios jurídicos, conferindo segurança jurídica, autenticidade e fé-pública.</p>
			<p>Apesar de habitualmente as escrituras públicas serem vinculadas à transmissão imobiliária, seu horizonte é muito mais amplo!</p>
			<p>Todo e qualquer ato e negócio jurídico podem ser formalizados por escritura pública, pois não há tipicidade para seu uso.</p>
			<p>Dentre os exemplos mais corriqueiros, podemos citar: união estável, entre pessoas do mesmo sexo ou sexo distintos; dependência econômica; "contratos" de namoro; e, declaração de vida para fins previdenciários.</p>
			<div class="content__title">
				<strong>Importante:</strong>
