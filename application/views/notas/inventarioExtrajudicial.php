            <h5><strong>Inventário Extrajudicial</strong></h5>
            <div class="content__title">
                <strong>Conceito</strong>
            </div>
            <p>Inventário extrajudicial é o procedimento pelo qual, via escritura pública, apuram-se e relacionam-se todos os bens e direitos pertencentes ao de cujus, incluindo dívidas, para distribuí-los entre seus sucessores.</p>
            <div class="content__title">
                <strong>Importante:</strong>
            </div>
            <p>
                <strong>Art. 610, CPC.</strong>
                Havendo testamento ou interessado incapaz, proceder-se-á ao inventário judicial.
            </p>
            <p>§ 1º Se todos forem capazes e concordes, o inventário e a partilha poderão ser feitos por escritura pública, a qual constituirá documento hábil para qualquer ato de registro, bem como para levantamento de importância depositada em instituições financeiras.</p>
            <p>§ 2º O tabelião somente lavrará a escritura pública se todas as partes interessadas estiverem assistidas por advogado ou por defensor público, cuja qualificação e assinatura constarão do ato notarial.</p>
            <p>
                <strong>Item 116, NSCGJ.</strong>
                A escritura pública de inventário e partilha conterá a qualificação completa do autor da herança; o regime de bens do casamento; pacto antenupcial e seu registro imobiliário, se houver; dia e lugar em que faleceu o autor da herança; data da expedição da certidão de óbito; livro, folha, número do termo e unidade de serviço em que consta o registro do óbito; e a menção ou declaração dos herdeiros de que o autor da herança não deixou testamento e outros herdeiros, sob as penas da lei.
            </p>
            <p>
                <strong>Item 129, NSCGJ.</strong>
                Diante da expressa autorização do juízo sucessório competente, nos autos do procedimento de abertura e cumprimento de testamento, sendo todos os interessados capazes e concordes, poderão ser feitos o inventário e a partilha por escritura pública, que constituirá título hábil para o registro imobiliário.
            </p>
            <p>
                <strong>129.1.</strong>
                Poderão ser feitos o inventário e a partilha por escritura pública, também, nos casos de testamento revogado ou caduco, ou quando houver decisão judicial, com trânsito em julgado, declarando a invalidade do testamento, observadas a capacidade e a concordância dos herdeiros.
            </p>
