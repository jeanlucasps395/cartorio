			<h5><strong>Ata Notarial</strong></h5>
			<div class="content__title">
				<strong>Conceito</strong>
			</div>
			<p>"É o documento escrito pelo Tabelião que prova a existência de um fato ou situação, cujo contexto seja importante perpetuar para momento futuro, como por exemplo:</p>
			<p>. Perpetuar conteúdo de páginas da internet;</p>
			<p>. Comprovar presença de pessoas em certos lugares;</p>
			<p>. Atestar estado de imóveis no início ou fim de locação;</p>
			<p>. Comprovar entrega de documentos ou coisas;</p>
			<p>. Certificar existência de pessoa (chamada de ata de fé de vida); e,</p>
			<p>. Atestar apelido ou profissão de pessoa."</p>
			<p><i>(SERVIÇOS NOTARIAIS E DE REGISTRO - Corregedoria Geral da Justiça de São Paulo)</i></p>
			<div class="content__title">
				<strong>Importante:</strong>
			</div>
			<p>
				<strong>Art. 384, CPC.</strong>
				A existência e o modo de existir de algum fato podem ser atestados ou documentados, a requerimento do interessado, mediante ata lavrada por tabelião.
			</p>
			<p><i>Parágrafo único.</i> Dados representados por imagem ou som gravados em arquivos eletrônicos poderão constar da ata notarial.</p>
			<p>
				<strong>Art. 216-A</strong>
				LRP. Sem prejuízo da via jurisdicional, é admitido o pedido de reconhecimento extrajudicial de usucapião, que será processado diretamente perante o cartório do registro de imóveis da comarca em que estiver situado o imóvel usucapiendo, a requerimento do interessado, representado por advogado, instruído com:
			</p>
			<p>I - ata notarial lavrada pelo tabelião, atestando o tempo de posse do requerente e seus antecessores, conforme o caso e suas circunstâncias;</p>
			<p>II - planta e memorial descritivo assinado por profissional legalmente habilitado, com prova de anotação de responsabilidade técnica no respectivo conselho de fiscalização profissional, e pelos titulares de direitos reais e de outros direitos registrados ou averbados na matrícula do imóvel usucapiendo e na matrícula dos imóveis confinantes;</p>
			<p>III - certidões negativas dos distribuidores da comarca da situação do imóvel e do domicílio do requerente;</p>
			<p>IV - justo título ou quaisquer outros documentos que demonstrem a origem, a continuidade, a natureza e o tempo da posse, tais como o pagamento dos impostos e das taxas que incidirem sobre o imóvel.</p>
			<p>
				<strong>Item 137, NSCGJ</strong>
				Ata notarial é a narração objetiva, fiel e detalhada de fatos jurídicos presenciados ou verificados pessoalmente pelo Tabelião de Notas.
			</p>
			<p>
				<strong>137.1</strong>
				A ata notarial é documento dotado de fé pública.
			</p>
			<p>
				<strong>137.2</strong>
				A ata notarial será lavrada no livro de notas.
			</p>
			<p>
				<strong>Item 139, NSCGJ.</strong>
				A ata notarial poderá:
			</p>
			<p>a) conter a assinatura do solicitante e de eventuais testemunhas;</p>
			<p>b) ser redigida em locais, datas e horas diferentes, na medida em que os fatos se sucedam, com descrição fiel do presenciado e verificado, e respeito à ordem cronológica dos acontecimentos e à circunscrição territorial do Tabelião de Notas;</p>
			<p>c) conter relatórios ou laudos técnicos de profissionais ou peritos, que serão qualificados e, quando presentes, assinarão o ato;</p>
			<p>d) conter imagens e documentos em cores, podendo ser impressos ou arquivados em classificador próprio.</p>
