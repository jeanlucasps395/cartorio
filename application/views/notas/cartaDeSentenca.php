			<h5><strong>Carta de Sentença Notarial</strong></h5>
			<div class="content__title">
				<strong>Conceito</strong>
			</div>
			<p>"A carta de sentença não integra, nem completa, nem é requisito de validade da decisão judicial. A carta de sentença é mero instrumento, útil ao cumprimento da decisão judicial. Trata-se, em verdade, de mero conjunto de cópias autenticadas dos autos judiciais sobre as quais se aplicam cautelas para evitar adulteração (supressão, acréscimo ou substituição de peças), o que se obtém com a numeração, rubrica, termo de abertura e de encerramento, e autuação. Trata-se de um veículo para o cumprimento das ordens judiciais, diante da inviabilidade de utilização dos autos originais para esse fim. Deve retratar o que se decidiu no processo judicial, e permitir a exata compreensão da ordem, de quem seja seu destinatário, e de qual seja seu objeto", esclarece Antônio Carlos Alves Braga Júnior, Juiz de Direito.</p>
			<div class="content__title">
				<strong>Importante:</strong>
			</div>
			<p>
				<strong>Item 213, NSCGJ.</strong>
				O Tabelião de Notas poderá, a pedido da parte interessada, formar cartas de sentença das decisões judiciais, dentre as quais, os formais de partilha, as cartas de adjudicação e de arrematação, os mandados de registro, de averbação e de retificação, nos moldes da regulamentação do correspondente serviço judicial.
			</p>
			<p>
				<strong>213.1.</strong>
				As peças instrutórias das cartas de sentença deverão ser extraídas dos autos judiciais originais, ou do processo judicial eletrônico, conforme o caso.
			</p>
			<p>
				<strong>213.3.</strong>
				O termo de abertura deverá conter a relação dos documentos autuados, e o termo de encerramento informará o número de páginas da carta de sentença. Ambos serão considerados como uma única certidão para fins de cobrança de emolumentos.
			</p>
			<p>
				<strong>213.4.</strong>
				O tabelião fará a autenticação de cada cópia extraída dos autos do processo judicial, atendidos os requisitos referentes à prática desse ato, incluídas a aposição de selo de autenticidade e cobrança dos emolumentos.
			</p>
			<p>
				<strong>213.5.</strong>
				A carta de sentença deverá ser formalizada no prazo máximo de 5 (cinco) dias, contados da solicitação do interessado e da entrega dos autos originais do processo judicial, ou do acesso ao processo judicial eletrônico.
			</p>
			<p>
				<strong>Item 214, NSCGJ.</strong>
				Todas as cartas de sentença deverão conter, no mínimo, cópias das seguintes peças:
			</p>
			<p>I - sentença ou decisão a ser cumprida;</p>
			<p>II - certidão de transcurso de prazo sem interposição de recurso (trânsito em julgado), ou certidão de interposição de recurso recebido sem efeito suspensivo;</p>
			<p>III - procurações outorgadas pelas partes;</p>
			<p>IV - outras peças processuais que se mostrem indispensáveis ou úteis ao cumprimento da ordem, ou que tenham sido indicadas pelo interessado.</p>
			<p>
				<strong>Item 217, NSCGJ.</strong>
				A critério do interessado, as cartas de sentença poderão ser formadas em meio físico ou eletrônico, aplicando-se as regras relativas à materialização e desmaterialização de documentos pelo serviço notarial.
			</p>
