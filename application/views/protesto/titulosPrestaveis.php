            <h5><strong>Título Protestáveis</strong></h5>
            <p>Segundo o artigo 1º, da Lei Federal nº 9.492/97, são passíveis de protesto os título de crédito e os outros documentos de dívida.</p>
            <p>
                <strong>Art. 1</strong>
                º Protesto é o ato formal e solene pelo qual se prova a inadimplência e o descumprimento de obrigação originada em <strong>títulos e outros documentos de dívida.</strong>
            </p>
            <p>
                <strong>Parágrafo único.</strong>
                Incluem-se entre os títulos sujeitos a protesto as certidões de dívida ativa da União, dos Estados, do Distrito Federal, dos Municípios e das respectivas autarquias e fundações públicas.
            </p>
            <p>São exemplos de títulos de crédito: letras de câmbio, cheques, duplicatas (mercantis e de prestação de serviços), notas promissórias e cédulas de crédito bancário.</p>
            <p>Quanto aos outros documentos de dívida, importante a leitura do Capítulo XV, das NSCGJ de São Paulo:</p>
            <p>
                <strong>Item 22, NSCGJ.</strong>
                Além dos considerados títulos executivos, também são protestáveis outros documentos de dívida dotados de certeza, liquidez e exigibilidade, atributos a serem valorados pelo Tabelião, com particular atenção, no momento da qualificação notarial.
            </p>
            <p>Observamos que todos os títulos executivos judiciais e extrajudiciais, previstos no CPC, são passíveis de apontamento, além daqueles documentos dotados de certeza, liquidez e exigibilidade. Há um universo gigante de alternativas, sendo exemplos relevantes de outros documentos de dívida: decisões judiciais, contratos de locação de bens imóveis, contratos de honorários advocatícios, encargos condominiais, certidões de dívida ativa e instrumentos de confissão de dívida.</p>
