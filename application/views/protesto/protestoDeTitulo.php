            <h5><strong>Protesto de Títulos</strong></h5>
            <p>Nos dias atuais, toda e qualquer pessoa, busca mecanismos rápidos e eficazes, para que possa receber valores devidos.</p>
            <p>
                Diante dessa necessidade, surge como importante mecanismo de cobrança, o protesto extrajudicial, que tem o seguinte conceito: "Protesto é o ato formal e solene pelo qual se prova a inadimplência e o descumprimento de obrigação originada em títulos e outros documentos de dívida" (art. 1º da Lei Federal n º 9.492, de 10 de setembro de 1997).
            </p>
            <strong><i>Vantagens do Protesto:</i></strong>
            <p>1-) <strong>GRATUIDADE</strong> - o credor <strong>não paga nada para protestar</strong>; só terá custo quando desistir do protesto. Logo, salvo nessa única hipótese, <strong>PROTESTAR É DE GRAÇA PARA O CREDOR!!!</strong></p>
            <p>2-) <strong>RAPIDEZ</strong> - em um curto prazo <strong>(máximo de 3 dias úteis)</strong>, há resposta quanto ao pagamento do valor devido.</p>
            <p>3-) <strong>EFICÁCIA</strong> - boa parte dos devedores, temerosos de ter seu nome protestado, e consequentemente inscrito no SERASA, efetuam o pagamento no prazo constante de intimação.</p>
            <p>4-) <strong>EDUCAÇÃO DOS DEVEDORES</strong> - cientes que medidas rápidas são tomadas contra inadimplentes, os devedores pensarão duas vezes antes de não pagar o que é devido.</p>
