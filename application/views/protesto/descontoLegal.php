            <h5><strong>Desconto Legal ME e EPP</strong></h5>
            <p>Você que é MICRO EMPRESÁRIO (ME) ou EMPRESÁRIO DE PEQUENO PORTE (EPP), possui desconto de quase 40%, no pagamento das custas do protesto.</p>
            <p>Solicite no balcão de protesto, sua inclusão como beneficiário do desconto.</p>
            <p>Abaixo, reproduzimos o artigo 73 da Lei Complementar 123/2006 que garante o desconto das microempresas e empresas de pequeno porte.</p>
            <strong> Do Protesto de Títulos</strong>
            <p><strong>Art. 73.</strong> O protesto de título, quando o devedor for microempresário ou empresa de pequeno porte, é sujeito às seguintes condições:</p>
            <ul>
                <li>I - sobre os emolumentos do tabelião não incidirão quaisquer acréscimos a título de taxas, custas e contribuições para o Estado ou Distrito Federal, carteira de previdência, fundo de custeio de atos gratuitos, fundos especiais do Tribunal de Justiça, bem como de associação de classe, criados ou que venham a ser criados sob qualquer título ou denominação, ressalvada a cobrança do devedor das despesas de correio, condução e publicação de edital para realização da intimação; </li>
                <li>II - para o pagamento do título em cartório, não poderá ser exigido cheque de emissão de estabelecimento bancário, mas, feito o pagamento por meio de cheque, de emissão de estabelecimento bancário ou não, a quitação dada pelo tabelionato de protesto será condicionada à efetiva liquidação do cheque; </li>
                <li>III - o cancelamento do registro de protesto, fundado no pagamento do título, será feito independentemente de declaração de anuência do credor, salvo no caso de impossibilidade de apresentação do original protestado; </li>
                <li>IV - para os fins do disposto no caput e nos incisos I, II e III do caput deste artigo, o devedor deverá provar sua qualidade de microempresa ou de empresa de pequeno porte perante o tabelionato de protestos de títulos, mediante documento expedido pela Junta Comercial ou pelo Registro Civil das Pessoas Jurídicas, conforme o caso; </li>
                <li>V - quando o pagamento do título ocorrer com cheque sem a devida provisão de fundos, serão automaticamente suspensos pelos cartórios de protesto, pelo prazo de 1 (um) ano, todos os benefícios previstos para o devedor neste artigo, independentemente da lavratura e registro do respectivo protesto. </li>
            </ul>
