			<div class="content__title">
				<strong>Importante</strong>
			</div>
			<p>
				<strong>Item 169, NSCGJ.</strong>
				O Tabelião de Notas, ao autenticar cópias reprográficas, não deve restringir-se à mera conferência dos textos ou ao aspecto morfológico da escrita, mas verificar, com cautela, se o documento copiado contém rasuras, supressão de palavras ou linhas ou, ainda, quaisquer outros sinais suspeitos indicativos de possíveis fraudes.
			</p>
			<p>
				Parágrafo único. Para os fins desta Resolução, entende-se como legalização, ou chancela consular, a formalidade pela qual se atesta a autenticidade da assinatura, da função ou do cargo exercido pelo signatário do documento e, quando cabível, a autenticidade do selo ou do carimbo nele aposto.
			</p>
			<p>
				<strong>Item 173, NSCGJ.</strong>
				Não será extraída, autenticada ou utilizada para a prática de ato notarial, reprodução reprográfica de outra reprodução reprográfica, autenticada ou não, de documento público ou particular.
			</p>
			<p>
				<strong>Item 176, NSCGJ.</strong>
				Não podem ser autenticados, dentre outros documentos:
			</p>
			<ol>
				<li>os transmitidos por fac-símile, exceto os que contenham assinatura inserida após a recepção do documento;</li>
				<li>parte ou partes de documentos cuja compreensão de seu conteúdo dependa de sua leitura integral;</li>
				<li>documentos escritos a lápis ou outro meio de impressão delével;</li>
				<li>documentos alterados com tinta corretiva, quando a correção implique substancial alteração do conteúdo do documento <i>(nome completo, datas, valores, etc.);</i></li>
